/**
 * Función que opera sobre tres valores de tipo numérico que le pasamos como 
 * parametros.
 * @param {type} first Primer valor.
 * @param {type} second Segundo valor.
 * @param {type} third Tercer valor.
 * @returns {unresolved} Resultado de operar los parametros de la función.
 */
function math(first, second, third) {
    // Retornamos el resultado obtenido de operar.
    return (second * third) + first;

}
// Mostramos por consola el resultado de ejecutar la función.
console.log(math(53, 61, 67));